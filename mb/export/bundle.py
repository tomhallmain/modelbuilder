def infer_layout_from_state_dict(state_dict) -> str:
    keys = list(state_dict.keys())
    if not keys:
        return "torchvision"
    # FastAI-style sequential exports often use 0.* / 1.* top-level keys.
    if any(k.startswith("0.") or k.startswith("1.") for k in keys):
        return "fastai_sequential"
    # torchvision training exports from ModelBuilder use layer*/conv1/fc style.
    return "torchvision"


