def _make_torchvision_backbone(architecture: str):
    # Version-safe torchvision constructor (supports old/new API).
    if not hasattr(models, architecture):
        raise ValueError(f"Unsupported torchvision architecture: {{architecture}}")
    ctor = getattr(models, architecture)
    try:
        return ctor(weights=None)
    except TypeError:
        # Older torchvision signatures used pretrained=False.
        return ctor(pretrained=False)


